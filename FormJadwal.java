    @Override
    public void actionPerformed(ActionEvent e) {
        String tgl = getDateMovie();
        for (int j = 0; j < days; j++) {
            if (dayArray[j] == e.getSource()) {
                studio.clear();
                DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
                LocalDate date = LocalDate.parse(tgl, formatter);
                LocalDate newDate = date.plusDays(j);
                for (int i = 0; i < countBtn; i++) {
                    conTime.remove(btnArray[i]);
                    conTime.revalidate();
                    conTime.repaint();
                }
                hriLbl.setText(newDate.getDayOfWeek().name() + ", ");
                tglLbl.setText(newDate.format(formatter));
                menuList(newDate.format(formatter), id_film);
            }
        }
        String jam = "";
        DataMovie data;
        for (int i = 0; i < countBtn; i++) {
            if (btnArray[i] == e.getSource()) {
                System.out.println(studio);
                jam = btnArray[i].getText();
                data = dataMovie(id_film, getTextTgl(), jam,studio.get(i));
                dispose();
                new FormPetaKursi(data, db).setVisible(true);
            }
        }
    }
