    private void dbContent() {
        try {
            db.rs = db.perintah.executeQuery("SELECT nama_film,durasi_menit,date,desc_film,id_waktu,image FROM"
                    + " jadwal,film WHERE jadwal.id_film = '" + id_film + "' AND film.id_film ='" + id_film + "'");
            if (db.rs.next()) {
                byte[] img = db.rs.getBytes("image");
                InputStream inputStream = new ByteArrayInputStream(img);
                BufferedImage images = ImageIO.read(inputStream);
                Judul.setText(db.rs.getString("nama_film"));
                durasi.setText(db.rs.getString("durasi_menit") + " menit");
                txtDesc.setText(db.rs.getString("desc_film"));
                banner_img.setText("");
                banner_img.setIcon(new ImageIcon(images));
                Icon icn = banner_img.getIcon();
                ImageIcon icon = (ImageIcon) icn;
                Image image = icon.getImage().getScaledInstance(banner_img.getWidth(), banner_img.getHeight(), Image.SCALE_SMOOTH);
                banner_img.setIcon(new ImageIcon(image));
            }
        } catch (SQLException e) {
            System.err.println(e);
        } catch (IOException e) {
            System.err.println("seez");
        }
    }

    private void menuList(String tgl, String id) {
        // Button Wktu
        String waktu = "";
        try {
            db.rs = db.perintah.executeQuery("SELECT COUNT(*) as jumlah FROM jadwal RIGHT JOIN waktu ON waktu.id_waktu = jadwal.id_waktu"
                    + " WHERE date='" + tgl + "' AND id_film='" + id + "'" + " ORDER BY jam_mulai ASC");
            if (db.rs.next()) {
                countBtn = db.rs.getInt("jumlah");
            }
            db.rs = db.perintah.executeQuery("SELECT *,DAYNAME(date) AS nama_hari FROM jadwal RIGHT JOIN waktu ON waktu.id_waktu = jadwal.id_waktu"
                    + " RIGHT JOIN harga ON harga.id_hari=jadwal.id_hari INNER JOIN studio ON studio.id_studio=jadwal.id_studio WHERE date='" + tgl + "' AND id_film='" + id + "'" + " ORDER BY jam_mulai ASC");
            for (int i = 0; i < countBtn; i++) {
                if (db.rs.next()) {
                    waktu = db.rs.getString("jam_mulai");
                    studio.add(db.rs.getString("id_studio"));
                }
                btnArray[i] = new JButton(waktu);
                btnArray[i].setCursor(new Cursor(HAND_CURSOR));
                btnArray[i].addActionListener(this);
                conTime.add(btnArray[i]);

                // Harga Label
                Locale myIndonesianLocale = new Locale("in", "ID");
                NumberFormat rupiah = NumberFormat.getCurrencyInstance(myIndonesianLocale);
                priceLbl.setText(rupiah.format(db.rs.getInt("harga")));
            }
        } catch (SQLException e) {
            System.err.println(e);
        }
    }

    private String getDateMovie() {
        String dm = "";
        try {
            db.rs = db.perintah.executeQuery("SELECT jadwal.id_film,film.durasi_menit,jam_mulai, jadwal.date, DAYNAME(jadwal.date) as nama_hari "
                    + "FROM film RIGHT JOIN jadwal ON film.id_film=jadwal.id_film "
                    + "RIGHT JOIN waktu ON waktu.id_waktu = jadwal.id_waktu"
                    + " WHERE jadwal.id_film = '" + id_film + "'");
            if (db.rs.next()) {
                dm = db.rs.getString("date");
                return dm;
            }
        } catch (SQLException e) {
            System.err.println(e);
        }
        return dm;
    }

    private DataMovie dataMovie(String id, String tgl, String jam,String studio) {
        DataMovie dam = null;
        try {
            db.rs = db.perintah.executeQuery("SELECT * FROM jadwal LEFT JOIN waktu ON "
                    + "jadwal.id_waktu=waktu.id_waktu  INNER JOIN studio ON studio.id_studio=jadwal.id_studio"
                    + " WHERE id_film='" + id + "' and date='" + tgl + "' and jam_mulai='" + jam + "' and jadwal.id_studio='" +studio+ "'");
            if (db.rs.next()) {
                dam = new DataMovie(db.rs.getString("id_jadwal"),
                        db.rs.getString("id_studio"), db.rs.getString("id_film"),
                        db.rs.getString("date"), db.rs.getString("jam_mulai"));
                return dam;
            }
        } catch (SQLException e) {
            System.err.println("siuu");
        }
        return dam;
    }

    private void initialize() {
        dbContent();
        input = getDateMovie();
        menuList(input, id_film);
        // Buttons Hari Senin - Minggu
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("yyyy-MM-dd");
        LocalDate date = LocalDate.parse(input, formatter);
        for (int i = 0; i < days; i++) {
            LocalDate newDate = date.plusDays(i);
            dayArray[i] = new JButton(newDate.getDayOfWeek().name());
            dayArray[i].setCursor(new Cursor(HAND_CURSOR));
            dayArray[i].addActionListener(this);
            conHari.add(dayArray[i]);
            hriLbl.setText(date.getDayOfWeek().name() + ", ");
            tglLbl.setText(date.format(formatter));
        }
    }

