    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here
        if (jTextField1.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Silahkan lengkapi data ", "informasi", JOptionPane.INFORMATION_MESSAGE);
            jTextField1.requestFocus();
        } else if (jTextField2.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Silahkan lengkapi data ", "informasi", JOptionPane.INFORMATION_MESSAGE);
            jTextField2.requestFocus();
        } else {
            if (modesimpan == true) {
                Dataditemukan = cariData(jTextField1.getText());
                if (Dataditemukan == true) {
                    JOptionPane.showMessageDialog(this, "Penyimpnan data jurusan gagal dilakukan karena id studio !" + jTextField1.getText() + "Telah ada di tabel", "informasi", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    try {
                        db.perintah.execute("INSERT INTO waktu VALUES('"
                                + jTextField1.getText() + "','"
                                + jTextField2.getText() + "')"
                        );
                        tampilData();
                    } catch (SQLException e) {
                        JOptionPane.showMessageDialog(this, "Penyimpanan data studio gagal dilakukan ", "informasi", JOptionPane.INFORMATION_MESSAGE);
                    }
                }
            } else {
                if ((jTable1.getSelectedRow() == -1) && (Dataditemukan == false)) {
                    JOptionPane.showMessageDialog(this, "Silahkan Pilih baris yang akan diubah", "Informasi", JOptionPane.INFORMATION_MESSAGE);

                } else {
                    try {
                        db.perintah.execute("UPDATE waktu SET jam_mulai= '" + jTextField2.getText() + "' " + "WHERE id_waktu ='" + jTextField1.getText() + "' ");

                        tampilData();
                    } catch (SQLException e) {
                        System.err.println("Query Error"); // Gantilah dengan penanganan kesalahan yang sesuai dengan aplikasi Anda.
                    }

                }
            }
            clear();
            jTextField1.setEnabled(false);
            jTextField2.setEnabled(false);
            jButton1.setEnabled(true);
            jButton1.requestFocus();
            jButton2.setEnabled(false);
            jButton3.setEnabled(false);
            jButton4.setEnabled(false);
            jButton5.setEnabled(false);

        }
    }//GEN-LAST:event_jButton2ActionPerformed

