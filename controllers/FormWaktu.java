    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (jTextField1.getText().length() < 6) {
                JOptionPane.showConfirmDialog(this,
                        "Minimal ID harus 6 Angka", "Konfirmasi",
                        JOptionPane.PLAIN_MESSAGE, JOptionPane.OK_OPTION
                );
            } else {
                Dataditemukan = cariData(jTextField1.getText());
                if (Dataditemukan) {
                    int pilihan;
                    pilihan = JOptionPane.showConfirmDialog(this, "ID Waktu " + jTextField1.getText() + " Sudah ada ! " + "Apakah anda ingin mengubah / menghapus data?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
                    if (pilihan == JOptionPane.YES_OPTION) {
                        try {
                            // Assuming rs is a ResultSet declared earlier
                            jTextField1.setText(db.rs.getString("id_waktu"));
                            jTextField2.setText(db.rs.getString("jam_mulai"));
                            jTextField1.setEnabled(false);
                            jTextField2.setEnabled(false);
                            jButton1.setEnabled(false);
                            jButton2.setEnabled(false);
                            jButton3.setEnabled(true);
                            jButton3.requestFocus();
                            jButton4.setEnabled(true);
                            jButton5.setEnabled(true);

                        } catch (SQLException e) {
                            System.err.println("Query Error");
                        }
                    } else {
                        jTextField1.setText("");
                        jTextField1.requestFocus();
                    }
                } else {
                    jTextField2.setEnabled(true);
                    jTextField2.requestFocus();
                    jButton2.setEnabled(true);
                }
            }
        }
    }//GEN-LAST:event_jTextField1KeyReleased

